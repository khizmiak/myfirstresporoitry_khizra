# Upper case 
x = "hi my name is khizra zahid and i'm 19 years old"
print(x.upper())

#Lowercase
x = "HI MY NAME IS KHIZRA ZAHID AND I'M 19 YEARS OLD"
print(x.lower())

# Removing Whitespace

x = "   Khizra, zahid "
print(x.strip())  

# Replace string 

x = "im from sialkot. This is a great city."
print(x.replace("This" , "which"))

# Split string 

x = "This, is, a , great, city"
print(x.split(","))
